--Returns: number - Num talents in given tree
function TInspect_GetNumTalents(classindex, tabindex)
	return table.getn(TInspect_TalentDB[classindex].trees[tabindex].talents);
end

--Returns: table - Talent information
function TInspect_GetTalentInfo(classindex, tabindex, talentindex)
	return TInspect_TalentDB[classindex].trees[tabindex].talents[talentindex];
end

--Returns: string - Tree name, string - Background image
